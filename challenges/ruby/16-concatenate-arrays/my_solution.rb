# Concatenate Two Arrays
# Define un metodo `array_concat` el cual tome dos `Arrays`
#  como argumentos y devuelva un solo `Array` producido por 
#  concatener los argumentos.

# Por ejemplo,

# ```ruby
# array_concat([1,2,3], [4,5,6])   # => [1,2,3,4,5,6]
# array_concat([10, 4], ["apple"]) # => [10, 4, "apple"]
# ```
# Tu solucion abajo:

def array_concat(a,b)
	a.concat b
end






